 function selectAll() {
	 var opt_select =document.dataForm.ex_selectAll.checked;
    var aElems = document.getElementsByName("opt");
    for ( var i=0;  i < aElems.length; i++ ) 
    {
		if  ( aElems[i].type == "checkbox" && aElems[i].disabled==false) {
			aElems[i].checked = opt_select;
		}
    }

    return;
 }
