  @Override
  public void dataRead() throws Exception {
    if (controlTabName.length() == 0) {
      if (wp.colStr("control_tab_name").length() == 0)
        controlTabName = orgTabName;
      else
        controlTabName = wp.colStr("control_tab_name");
    } else {
      if (wp.colStr("control_tab_name").length() != 0)
        controlTabName = wp.colStr("control_tab_name");
    }
    wp.selectSQL = "hex(a.rowid) as rowid," + "a.serial_no," + "a.trans_month," + "a.file_date,"
        + "a.error_type," + "a.error_desc," + "'ecs 資料' as aa," + "'高鐵對帳異資料' as bb,"
        + "a.ecs_trans_type," + "a.thsr_trans_type," + "a.ecs_trans_date," + "a.thsr_trans_date,"
        + "a.ecs_trans_time," + "a.thsr_trans_time," + "a.ecs_org_trans_date,"
        + "a.thsr_org_trans_date," + "a.ecs_pay_cardid," + "a.thsr_pay_cardid,"
        + "a.ecs_authentication_code," + "a.thsr_authentication_code," + "a.ecs_pnr,"
        + "a.thsr_pnr," + "a.ecs_ticket_id," + "a.thsr_ticket_id," + "a.ecs_trans_amount,"
        + "a.thsr_trans_amount," + "a.ecs_train_no," + "a.thsr_train_no," + "a.ecs_seat_no,"
        + "a.thsr_seat_no," + "a.ecs_org_serial_no," + "a.thsr_org_serial_no";

    wp.daoTable = controlTabName + " a ";
    wp.whereStr = "where 1=1 ";
    if (qFrom == 0) {
      wp.whereStr = wp.whereStr;
    } else if (qFrom == 1) {
      wp.whereStr = wp.whereStr + sqlRowId(rowid, "a.rowid");
    }

    pageSelect();
    if (sqlNotFind()) {
      alertErr2("查無資料, key= " + "[" + rowid + "]");
      return;
    }
    commErrorType("comm_error_type");
    commTransType("comm_ecs_trans_type");
    commTransType1("comm_thsr_trans_type");
    checkButtonOff();
  }

