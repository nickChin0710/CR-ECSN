  @Override
  public void queryFunc() throws Exception {
    String lsWhere = " where 1=1 " + sqlCol(wp.itemStr2("ex_acct_month1"), "acct_month", ">=")
        + sqlCol(wp.itemStr2("ex_acct_month2"), "acct_month", "<=");

    wp.whereStr = lsWhere;
    wp.queryWhere = wp.whereStr;
    wp.setQueryMode();

    queryRead();
  }

  @Override
  public void queryRead() throws Exception {
    wp.pageControl();

    wp.selectSQL = "" + " acct_month ," + " dest_amt ," + " feedback_amt ," + " autoadd_amt ,"
        + " autoadd_fee ";

    wp.daoTable = " ich_stmt_hst ";
    wp.whereOrder = " order by acct_month Asc ";

    pageQuery();

    if (sqlNotFind()) {
      alertErr2("此條件查無資料");
      return;
    }

    wp.setListCount(0);
    wp.setPageValue();

  }

