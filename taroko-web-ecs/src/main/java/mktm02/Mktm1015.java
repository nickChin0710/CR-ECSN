  public void initButton() {
    if (wp.respHtml.indexOf("_detl") > 0) {
      this.btnModeAud();
    }
  }

  // ************************************************************************
  @Override
  public void dddwSelect() {
    String lsSql = "";
    try {
      if ((wp.respHtml.equals("mktm1015_detl"))) {
        wp.optionKey = "";
        wp.initOption = "";
        if (wp.colStr("ref_ip_code").length() > 0) {
          wp.optionKey = wp.colStr("ref_ip_code");
        }
        if (wp.colStr("ref_ip_code").length() > 0) {
          wp.initOption = "--";
        }
        this.dddwList("dddw_ref_ip_code", "ecs_ref_ip_addr", "trim(ref_ip_code)", "trim(ref_name)",
            " where ref_ip_code like 'PARK%'");
      }
    } catch (Exception ex) {
    }
  }

